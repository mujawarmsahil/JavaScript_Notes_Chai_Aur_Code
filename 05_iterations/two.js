// while 
let index = 1;
while (index <= 10) {
    console.log(`value of index ${index}`);
    index++;
}

//in this condition is checked first and then the block is executed
// do while
let i = 1;
do {
    console.log(`value of index ${i}`);
    i++;
} while (i <= 10);
// in this block executed first and then condition is being checked